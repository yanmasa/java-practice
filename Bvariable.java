class Bvariable {
    public static void main(String args[]){
        // 文字 シングルクオーテーション
        char a = 'a';
        // 文字列
        // \n 改行　\t タブ
        String msg;
        // 整数　byte short int long
        int x = 10;
        // longは数字の最後にL
        long y = 5555555555555L;
        // 浮動小数点数　float double
        double d = 123.123;
        // floatは数値の最後にF
        float f = 12.12F;
        // 論理値　boolean
        boolean b = true; //false
        msg = "Hello\nWorld\t!";
        System.out.println(msg); 
    }
}