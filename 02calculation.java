class calculation {
    public static void main (String args[]){
        // 演算 + - * / %
        // ++ --
        int i;
        i = 10/3;
        System.out.println(i);
        
        i = 10%3;
        System.out.println(i);
        
        int x = 5;
        x++;
        System.out.println(x);
        
        x--;
        System.out.println(x);
        
        x+= 12;
        System.out.println(x);
        
        // 文字列の結合 +
        String c1 = "あいう";
        System.out.println(c1 + "えお" );

    }
}